import UIKit

func isValidSudoku(_ board: [[Character]]) -> Bool {
    var seen = Set<String>()

    for r in 0 ... 8 {
        for c in 0 ... 8 {
            let current = board[r][c]

            if current == "." {
                continue
            }

            let row = "\(current) in row \(r)"
            let col = "\(current) in col \(c)"
            let box = "\(current) in box \(r/3)-\(c/3)"

            if seen.contains(row) {
                return false
            } else {
                seen.insert(row)
            }

            if seen.contains(col) {
                return false
            } else {
                seen.insert(col)
            }

            if seen.contains(box) {
                return false
            } else {
                seen.insert(box)
            }
        }
    }

    return true
}

